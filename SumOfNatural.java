//Write a program to get sum of first 10 natural number.

package com.oopexample;

public class SumOfNatural {

	public static void main(String[] args) {
	int i,sum=0;
	for(i=1;i<=10;i++)
	{
		sum=sum+i;
	}
System.out.println("The addition of first 10 natural numbers is "+sum);
	}

}
